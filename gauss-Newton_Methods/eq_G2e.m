function D_G2e = eq_G2e( X, A )

    D_G2e = zeros(9, 9);

    pA = X(1)^2 + X(2)^2 + X(3)^2;
    pB = X(2)^2 + X(4)^2 + X(5)^2;
    pC = X(3)^2 + X(5)^2 + X(6)^2;
    pD = 2 * (X(1)*X(2) + X(2)*X(4) + X(3)*X(5));
    pE = 2 * (X(2)*X(3) + X(4)*X(5) + X(5)*X(6));
    pF = 2 * (X(1)*X(3) + X(2)*X(5) + X(3)*X(6));

    A(1) = A(1) - X(7);
    A(2) = A(2) - X(8);
    A(3) = A(3) - X(9);

    D_G2e(1, 1) = 2 * A(1)^2;
    D_G2e(2, 1) = 2 * A(1) * A(2);
    D_G2e(3, 1) = 2 * A(1) * A(3);
    D_G2e(4, 1) = 0;
    D_G2e(5, 1) = 0;
    D_G2e(6, 1) = 0;
    D_G2e(7, 1) = -2 * (X(1) * A(1) + X(2) * A(2) + X(3) * A(3));
    D_G2e(8, 1) = -2 * X(2) * A(1);
    D_G2e(9, 1) = -2 * X(3) * A(1);

    D_G2e(1, 2) = 2 * A(1) * A(2);
    D_G2e(2, 2) = D_G2e(2, 1);
    D_G2e(3, 2) = 2 * A(2) * A(3);
    D_G2e(4, 2) = D_G2e(2, 1);
    D_G2e(5, 2) = D_G2e(3, 1);
    D_G2e(6, 2) = 0;
    D_G2e(7, 2) = -2 * (2 * X(2) * A(1) + (X(1) + X(4)) * A(2) + X(5) * A(3));
    D_G2e(8, 2) = -2 * ((X(1) + X(4)) * A(1) + 2 * X(2) * A(2) + X(3) * A(3));
    D_G2e(9, 2) = -2 * (X(5) * A(1) + X(3) * A(2));

    D_G2e(1, 3) = D_G2e(3, 1);
    D_G2e(2, 3) = D_G2e(3, 2);
    D_G2e(3, 3) = 2 * (A(1)^2 + A(3)^2);
    D_G2e(4, 3) = 0;
    D_G2e(5, 3) = D_G2e(2, 1);
    D_G2e(6, 3) = D_G2e(3, 1);
    D_G2e(7, 3) = -2 * (2 * X(3) * A(1) + X(5) * A(2) + (X(1) + X(6)) * A(3));
    D_G2e(8, 3) = -2 * (X(5) * A(1) + X(2) * A(3));
    D_G2e(9, 3) = -2 * ((X(1) + X(6)) * A(1) + X(2) * A(2) + 2 * X(3) * A(3));

    D_G2e(1, 4) = 0;
    D_G2e(2, 4) = D_G2e(2, 1);
    D_G2e(3, 4) = 0;
    D_G2e(4, 4) = 2 * A(2)^2;
    D_G2e(5, 4) = D_G2e(3, 2);
    D_G2e(6, 4) = 0;
    D_G2e(7, 4) = -2 * X(2) * A(2);
    D_G2e(8, 4) = -2 * (X(2) * A(1) + X(4) * A(2) + X(5) * A(3));
    D_G2e(9, 4) = -2 * X(5) * A(2);

    D_G2e(1, 5) = 0;
    D_G2e(2, 5) = D_G2e(3, 1);
    D_G2e(3, 5) = D_G2e(2, 1);
    D_G2e(4, 5) = D_G2e(3, 2);
    D_G2e(5, 5) = 2 * (A(2)^2 + A(3)^2);
    D_G2e(6, 5) = D_G2e(3, 2);
    D_G2e(7, 5) = -2 * (X(3) * A(2) + X(2) * A(3));
    D_G2e(8, 5) = -2 * (X(3) * A(1) + 2 * X(5) * A(2) + (X(4) + X(6)) * A(3));
    D_G2e(9, 5) = -2 * (X(2) * A(1) + (X(4) + X(6)) * A(2) + 2 * X(5) * A(3));

    D_G2e(1, 6) = 0;
    D_G2e(2, 6) = 0;
    D_G2e(3, 6) = D_G2e(3, 1);
    D_G2e(4, 6) = 0;
    D_G2e(5, 6) = D_G2e(3, 2);
    D_G2e(6, 6) = 2 * A(3)^2;
    D_G2e(7, 6) = -2 * X(3) * A(3);
    D_G2e(8, 6) = -2 * X(5) * A(3);
    D_G2e(9, 6) = -2 * (X(3) * A(1) + X(5) * A(2) + X(6) * A(3));

    D_G2e(1, 7) = D_G2e(7, 1);
    D_G2e(2, 7) = D_G2e(7, 2);
    D_G2e(3, 7) = D_G2e(7, 3);
    D_G2e(4, 7) = D_G2e(7, 4);
    D_G2e(5, 7) = D_G2e(7, 5);
    D_G2e(6, 7) = D_G2e(7, 6);
    D_G2e(7, 7) = 2 * pA;
    D_G2e(8, 7) = pD;
    D_G2e(9, 7) = pF;

    D_G2e(1, 8) = D_G2e(8, 1);
    D_G2e(2, 8) = D_G2e(8, 2);
    D_G2e(3, 8) = D_G2e(8, 3);
    D_G2e(4, 8) = D_G2e(8, 4);
    D_G2e(5, 8) = D_G2e(8, 5);
    D_G2e(6, 8) = D_G2e(8, 6);
    D_G2e(7, 8) = pD;
    D_G2e(8, 8) = 2 * pB;
    D_G2e(9, 8) = pE;

    D_G2e(1, 9) = D_G2e(9, 1);
    D_G2e(2, 9) = D_G2e(9, 2);
    D_G2e(3, 9) = D_G2e(9, 3);
    D_G2e(4, 9) = D_G2e(9, 4);
    D_G2e(5, 9) = D_G2e(9, 5);
    D_G2e(6, 9) = D_G2e(9, 6);
    D_G2e(7, 9) = pF;
    D_G2e(8, 9) = pE;
    D_G2e(9, 9) = 2 * pC;

end
