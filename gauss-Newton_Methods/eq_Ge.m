function D_Ge = eq_Ge( X, A )

    D_Ge = zeros(1, 9);

    pA = X(1)^2 + X(2)^2 + X(3)^2;
    pB = X(2)^2 + X(4)^2 + X(5)^2;
    pC = X(3)^2 + X(5)^2 + X(6)^2;
    pD = X(1)*X(2) + X(2)*X(4) + X(3)*X(5);
    pE = X(2)*X(3) + X(4)*X(5) + X(5)*X(6);
    pF = X(1)*X(3) + X(2)*X(5) + X(3)*X(6);

    A(1) = A(1) - X(7);
    A(2) = A(2) - X(8);
    A(3) = A(3) - X(9);

    D_Ge(1) =  2 * A(1) * (X(1) * A(1) + X(2) * A(2) + X(3) * A(3));
    D_Ge(2) =  2 * (X(2) * (A(1)^2 + A(2)^2) + (X(1) + X(4)) * A(1) * A(2) + X(3) * A(2) * A(3) + X(5) * A(3) * A(1));
    D_Ge(3) =  2 * (X(3) * (A(1)^2 + A(3)^2) + X(5) * A(1) * A(2) + X(2) * A(2) * A(3) + (X(1) + X(6)) * A(3) * A(1));
    D_Ge(4) =  2 * A(2) * (X(2) * A(1) + X(4) * A(2) + X(5) * A(3));
    D_Ge(5) =  2 * (X(5) * (A(2)^2 + A(3)^2) + X(3) * A(1) * A(2) + (X(4) + X(6)) * A(2) * A(3) + X(2) * A(3) * A(1));
    D_Ge(6) =  2 * A(3) * (X(3) * A(1) + X(5) * A(2) + X(6) * A(3));
    D_Ge(7) = -2 * (pA * A(1) + pD * A(2) + pF * A(3));
    D_Ge(8) = -2 * (pD * A(1) + pB * A(2) + pE * A(3));
    D_Ge(9) = -2 * (pF * A(1) + pE * A(2) + pC * A(3));

end
